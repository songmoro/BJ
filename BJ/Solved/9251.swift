let A = ["."] + readLine()!.map(String.init)
let B = ["."] + readLine()!.map(String.init)

let (N, M) = (A.count - 1, B.count - 1)

var dp = [[Int]](repeating: [Int](repeating: 0, count: M + 1), count: N + 1)

for i in 1...N {
    for j in 1...M {
        if A[i] == B[j] { dp[i][j] = dp[i - 1][j - 1] + 1 }
        else { dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]) }
    }
}

print(dp[N][M])
